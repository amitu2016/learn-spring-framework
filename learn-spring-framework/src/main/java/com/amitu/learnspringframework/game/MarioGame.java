package com.amitu.learnspringframework.game;

public class MarioGame {

	public void up() {
		System.out.println("Jump");
	}

	public void down() {
		System.out.println("Go inside hole");
	}

	public void left() {
		System.out.println("Move backward");
	}

	public void right() {
		System.out.println("Accelerate");
	}

}
